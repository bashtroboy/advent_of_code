def input
  @input ||= DATA.read.split(",").map(&:to_i)
end

def solve(days)
  ocean = Hash.new(0)

  input.tally.each do |k, v|
    ocean[k] = v
    print k
    puts " "
    print v
    puts " "
  end

  days.times do
    pond = Hash.new(0)

    ocean.each do |fish, count|
      if fish == 0
        pond[6] += count
        pond[8] += count
      else
        pond[fish - 1] += count
      end
    end

    # Migrate fish from pond to ocean..
    ocean = pond
  end

  ocean.values.sum
end

def part1
  solve(80)
end

def part2
  solve(256)
end

